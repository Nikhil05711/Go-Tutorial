package main

import (
	"fmt"
)

func main() {
	var value1 = 30
	var value2 = "Nikhil_chauhan"
	var value3 = 34.22

	fmt.Printf("The value of value1 is %d", value1)
	fmt.Printf("\n The type of value1 is %T", value1)

	fmt.Printf("\nThe value of value2 is %s", value2)
	fmt.Printf("\n The type of value2 is %T", value2)

	fmt.Printf("\nThe value of value3 is %f", value3)
	fmt.Printf("\n The type of value3 is %T", value3)
}
